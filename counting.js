
// Create string variable
str = "madam"
// Create counter variable for letter a
aCount = 0

// Loop through every letter in the string
for(let i = 0; i < str.length; i++){
  // If the letter is a
  if(str[i] == "a"){
    // Add 1 to the counter
    aCount += 1
  }
}

// Print in the console 
console.log(aCount)

